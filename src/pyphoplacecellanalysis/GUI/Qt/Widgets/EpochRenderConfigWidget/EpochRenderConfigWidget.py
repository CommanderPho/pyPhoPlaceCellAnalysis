    # Add this method to handle the remove request
    def on_remove_epoch_series(self, widget):
        """Handle a request to remove an epoch series
        
        Note: We do NOT emit sigAnyConfigChanged here because when a datasource is removed
        via remove_rendered_intervals(), it will emit sigRenderedIntervalsListChanged which
        triggers update_from_configs() to sync the widget. Emitting here would cause a
        circular update that can create duplicates.
        
        If removal is initiated from UI only (not from datasource removal), the parent
        should handle the datasource removal separately.
        """
        print(f'EpochRenderConfigsListWidget.on_remove_epoch_series(widget: {widget})')
        
        # Find the config name for this widget
        config_name = None
        widget_or_list = None
        
        for name, widget_or_widget_list in self.ui.out_render_config_widgets_dict.items():
            if isinstance(widget_or_widget_list, list):
                if widget in widget_or_widget_list:
                    config_name = name
                    widget_or_list = widget_or_widget_list
                    break
            elif widget_or_widget_list == widget:
                config_name = name
                widget_or_list = widget_or_widget_list
                break
        
        if config_name is not None:
            # Remove the widget from the layout
            if isinstance(widget_or_list, list):
                # If it's in a list, remove just that one widget
                idx = widget_or_list.index(widget)
                
                # Find the widget's parent layout (the horizontal layout inside the scroll area)
                parent_widget = widget.parent()
                if parent_widget is not None:
                    parent_layout = parent_widget.layout()
                    if parent_layout is not None:
                        parent_layout.removeWidget(widget)
                
                widget.deleteLater()
                widget_or_list.pop(idx)
                
                # If the list is now empty, remove the whole entry including container widget
                if len(widget_or_list) == 0:
                    # Find and remove the container widget from config_widget_layout
                    for i in range(self.ui.config_widget_layout.count()):
                        item = self.ui.config_widget_layout.itemAt(i)
                        if item is not None:
                            widget = item.widget()
                            if widget is not None and widget.objectName() == f"containerWidget[{config_name}]":
                                # Get the layout from the widget to clear nested widgets
                                layout = widget.layout()
                                if layout is not None:
                                    # Clear the layout's widgets before removing
                                    while layout.count():
                                        layout_item = layout.takeAt(0)
                                        layout_widget = layout_item.widget()
                                        if layout_widget is not None:
                                            layout_widget.deleteLater()
                                        layout_layout = layout_item.layout()
                                        if layout_layout is not None:
                                            # Recursively clear nested layouts
                                            clear_layout(layout_layout)
                                self.ui.config_widget_layout.removeWidget(widget)
                                widget.deleteLater()
                                break
                    
                    del self.ui.out_render_config_widgets_dict[config_name]
                    if config_name in self.configs:
                        del self.configs[config_name]
            else:
                # Remove the single widget (not in a list, so directly in config_widget_layout)
                self.ui.config_widget_layout.removeWidget(widget)
                widget.deleteLater()
                del self.ui.out_render_config_widgets_dict[config_name]
                if config_name in self.configs:
                    del self.configs[config_name]
            
            # Emit sigRemoveRequested to notify parent to remove the datasource
            # Do NOT emit sigAnyConfigChanged here to prevent circular updates.
            # The datasource removal flow (remove_rendered_intervals() → sigRenderedIntervalsListChanged)
            # already triggers proper widget updates via _on_update_rendered_intervals().
            self.sigRemoveRequested.emit(self, config_name)



